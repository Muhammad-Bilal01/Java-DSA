package com.firstprogram;

public class Primitives {
    public static void main(String[] args) {

        //  Datatypes and Primitives Values

        // Integer Types
        byte bit = 124;
        short age = 43;
        int num = 21;
        long bigNumber = 23_42_22_22_22_21_21_21_33L;

        // Decimal Values
        double marks = 78.9;
        float averaage = 45.8f;

        // Boolean
        boolean isPass = true;
        boolean haveEarn = false;

        System.out.println(bit);
        System.out.println(age);
        System.out.println(num);
        System.out.println(bigNumber);
        System.out.println(marks);
        System.out.println(averaage);
        System.out.println(isPass);
        System.out.println(haveEarn);

    }
}
